/** UI copy for /events/[id] — toggles with global language (km / en). */

function t(isKhmer: boolean, km: string, en: string) {
  return isKhmer ? km : en;
}

export type EditEventTypeKey = 'WEDDING' | 'CEREMONY' | 'BIRTHDAY' | 'HOUSEWARMING' | 'FUNERAL';

const EDIT_EVENT_TYPE_EN: Record<EditEventTypeKey, string> = {
  WEDDING: 'Wedding',
  CEREMONY: 'Ceremony',
  BIRTHDAY: 'Birthday',
  HOUSEWARMING: 'Housewarming',
  FUNERAL: 'Funeral',
};

export function getEditEventTypeLabel(value: string, isKhmer: boolean, khmerLabel: string) {
  if (isKhmer) return khmerLabel;
  return EDIT_EVENT_TYPE_EN[value as EditEventTypeKey] || value;
}

